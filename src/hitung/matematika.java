package hitung;

public class matematika {
    //Variabel
    private double bil1, bil2;
    
    //Construktor
    public matematika(double bil1, double bil2){
        this.bil1 = bil1;
        this.bil2 = bil2;
    }
    
    //method
    public double setpenjumlahan(){
        return bil1 + bil1;
    }
    
    //method
    public double setpengurangan(){
        return bil1 - bil1;
    }
    
    //method
    public double setperkalian(){
        return bil1 * bil1;
    }
    
    //method
    public double setpembagiaan(){
        return bil1 / bil1;
    }
}
